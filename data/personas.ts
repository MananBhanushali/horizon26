const efSeed = (peak: number, peakRisk: number, selectedRisk: number) => {
  const points = [];
  for (let i = 0; i <= 24; i++) {
    const risk = 4 + i * 1.0;
    const distance = (risk - peakRisk) / 12;
    const r = peak - distance * distance * 6 + Math.cos(i * 0.7) * 0.18;
    points.push({
      risk,
      return: parseFloat(r.toFixed(2)),
      selected: Math.abs(risk - selectedRisk) < 0.6,
    });
  }
  return points;
};

